package com.ymens.spring.beans;

public class OrderItem {

        private int id;
        private Double price;

        public int getId() {
            return id;
        }

        public void setId(int id) {
            this.id = id;
        }

        public Double getPrice() {
            return price;
        }

        public void setPrice(Double price) {
            this.price = price;
        }
}
